#include "minishell.h"

int
	exp_is_var(char c)
{
	if (
		(c >= 'A' && c <= 'Z') ||
		(c >= 'a' && c <= 'z') ||
		(c >= '0' && c <= '9') ||
		(c == '_')
	)
		return (1);
	return (0);
}
